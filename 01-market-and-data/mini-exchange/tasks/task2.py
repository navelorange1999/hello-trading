## Task 2: 实现撮合引擎核心逻辑

# **做什么**: 当一个新订单进入时，检查是否能与对手方订单成交。实现「价格优先、时间优先」的撮合规则。

# **思考提示**:
# - 一个大买单可能需要吃掉多个卖单才能完全成交，这叫什么？你的撮合逻辑怎么处理这种情况？
# - 如果买方出价 10.5 但最优卖方要价是 10.3，成交价应该是多少？为什么？
# - 部分成交的订单应该怎么处理？删掉？修改？

# **验证**: 提交一系列买卖订单后，撮合引擎正确产生成交记录（成交价、成交量、买卖双方），未成交部分留在订单簿中。

# 📚 **关键资料**:
# - 搜索「price-time priority matching」了解撮合规则
# - 思考为什么全球几乎所有交易所都用这个规则（有没有例外？）

# ------------------------- 设计思考 -------------------------#

# Q: 撮合（matching）是订单簿自身的能力，还是一个独立引擎对订单簿的操作？
# A: 撮合应该在 add_order 之上加一层——先检查能否成交并撮合，不能撮合的部分再进订单簿。

# Q: 继承 / 组合 / 直接扩展，选哪种模式？
#
# | 模式         | 做法                                    | 适合场景                           |
# | A. 继承      | class MatchingOrderBook(OrderBook)      | 新类"是一种"原类，但行为不同       |
# | B. 组合      | 新类内部持有一个 OrderBook 实例          | 新类"使用"原类，但职责不同         |
# | C. 直接扩展  | 在 task1 的 OrderBook 上加方法           | 功能本来就属于它，只是分阶段实现   |
#
# 选 C。理由：
# - 撮合过程需要读写 OrderBook 的内部数据（bids/asks），需要添加新方法去修改它们
# - 这些操作本来就属于 OrderBook 自身的能力，不应该从外部去操作别人的内部数据（排除 B）
# - 也没必要把同一个东西分成两个类（排除 A）
# - 直接让 OrderBook 长出新能力最自然

# 实操结构：
# - task1.py 保持不变，是 OrderBook 的基础定义
# - task2.py 导入 task1 的 OrderBook，给它添加新方法，然后在这里测试撮合逻辑
# - tasks/ 目录需要 __init__.py 让它变成一个 Python 包

